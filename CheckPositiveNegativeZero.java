package Basic;

public class CheckPositiveNegativeZero {
	public static void main(String[] args)
	{
		int num=-1;
		
		if(num==0)
		{
			System.out.println(num +" is a Zero ");
		}
			
			if(num>=0)
			{
				System.out.println(num + " is a Positive number");
			}
			else
			{
				System.out.println(num +" is a Negative number");
			}
		
	}

}
